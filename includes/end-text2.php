<p>
「這次是我的勝利！姐姐！不知道你對咖哩的執念從何而來，但這次你就死心吧！」<br>
二十君緊緊抱著終於到手的東西不放，因為這段文字太長，二十君的神奇T-shirt也寫不下，所以二十君難得的開口說了這樣的一段話，顯示他內心之激動和對二十子的企圖的不滿。<br>
<br>
「還不是因為你想要把寶藏丟掉嘛！」二十子反嗆，顯然沒有成功贏得比賽和吃到咖哩讓他很不爽，但哪一件事情才是他不爽的主因，也只有二十子自己知道了。<br>
<br>
「這又不是什麼寶藏！這是、這是！」不擅言詞的二十君被激得說不出話來，這次神奇T-shirt也幫不了他。<br>
<br>
啾啾們在旁邊圍觀看戲，心裡一邊困惑著到底寶藏是指什麼東西呢？<br>
<br>
「總之我不想讓任何人看到這種黑歷史啦！連我自己看了都覺得很羞恥！姐姐就不要來多管閒事了！」<br>
「才不是黑歷史呢！這些明明是、明明是......」<br>
「明明是姐姐我在這世界上最喜歡、最喜歡的一篇漫畫啊！」<br>
<br>

聽到這樣的話語，二十君的T-shirt上一片空白，沒錯，在他懷中的，微微泛黃的、邊角有點捲起的、上面充滿塗改痕跡的，正是二十君第一次嘗試畫漫畫時留下的原稿。<br>
原稿上分崩離析的人物骨架、透視錯誤的背景，和沒有邏輯的分鏡，都顯示著這是一個完全的初心者的拙劣作品，完全沒值得欣賞之處，會被二十君認為是黑歷史也不為過。<br>
<br>
然而看遍各式各樣的漫畫，甚至自己就是一介同人大手的二十子卻說這是他最喜歡的漫畫。<br>
「因為這是二十君你第一次跟我說你是多麼的喜歡漫畫，第一次跟我說畫漫畫雖然很辛苦，但也很快樂，是二十君第一次為了自己的興趣去完成一件事情不是嗎？所以這篇漫畫，姐姐最喜歡了！」<br>
<br>
二十君保持沉默，但他頭上的呆毛卻彈跳了一下。<br>
他將手中的稿紙整理好，然後抬頭說：<br>
<br>
「姐姐，今天的晚餐吃咖哩炸豬排飯吧。」<br>
<br>
於是今天二十家的廚房再次飄出溫暖的咖哩香味，重修舊好的兩姐弟一起熱鬧的在狹窄的空間裡料理，熱愛炸豬排的啾啾們歡喜的啾啾叫。<br>
<br>
至於那份不再是秘密的寶藏？<br>
它們被慎重的裝回牛皮紙袋，收進二十姐弟的寶物匣中了。<br>
<br>
真是可喜可賀、可喜可賀。<br>

