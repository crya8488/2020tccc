<p>
這次是啾啾們率先抵達終點！果然人類的智慧遠遠比不上偉大的啾啾啊！<br>
所以到底是什麼寶藏導致二十姐弟之間的大亂鬥呢？<br>
就讓智慧的啾啾們來一探究竟。<br>
<br>
這是？<br>
出現在啾啾們面前的是一張張既熟悉又有點陌生的漫畫原稿，稿紙有點泛黃了，紙面上因為過度塗改導致纖維受損，像起毛球的衣料般凹凸不平，下筆過重使代針筆勾勒的線條周圍還留有反覆修改的深痕。<br>
<br>
黑白的連環漫畫講述著一對雙胞胎的故事，優秀而開朗的姐姐、低調而沉悶的弟弟，兩人擁有極為相似的面貌，卻有著南轅北轍的性格。像是仰望蒼天而鳴啼春光日景的籠鳥，像是日暮時分低喃思慕之情的寒蟬，弟弟總是沉默著、仰慕著另一個擁有相同面容的人，但他同時又是忌妒的、悲怒的，那個與自己血脈相連的人為何與自己差異如此巨大？<br>
<br>
啾啾們看著出自初學者手中的、技巧拙劣的，卻又流露出獨屬拙者的哀思的漫畫，這正是二十君第一次嘗試畫漫畫時的處女作。<br>
<br>
兩姐弟依然面對面的對峙著。<br>
怪盜與偵探，世界上最了解彼此，也最截然不同的兩個角色。<br>
偵探總是在追尋著怪盜的步伐，總是在解析怪盜留下的奇幻。<br>
而雙胞胎的弟弟，也總是在追尋他至親的胞姊的背影。<br>
<br>
「事到如今為什麼要把我的黑歷史重新翻找出來呢？是想要嘲笑我嗎？姐姐。」二十君的聲音一如往常的平靜。<br>
「不是的！我從來沒有想過要嘲笑你啊！明明不是這樣的，我又把事情搞砸了嗎？」二十子哭喪著臉，急切的辯駁，卻找不到合適的字眼來敘述心中的情感。<br>
「搞砸？像姐姐這樣優秀的人也會有做不好的事嗎？」<br>
「優秀？連弟弟的笑容都無法守護的姐姐還能被稱作優秀嗎？」<br>
<br>
「啾啾。」啾啾們相望，果真的是一對笨蛋姐弟啊，正是因為如此的在乎彼此，正是因為將對方視為最重要的人，所以才總是吵吵鬧鬧不是嗎？<br>
<br>
相似的面孔，露出相似的困惑表情，困惑的表情又同時變得更加複雜。
像是在哭泣，又像是在述說幸福。<br>

<br>
「姐姐總是能畫出美麗的線條、有趣的人物，和動人的故事，我最喜歡姐姐的漫畫了。」<br>
「姐姐我也最喜歡二十君的漫畫了。」<br>
「明明我畫的一點也不好。」<br>
「在我眼裡，你的好與不好都是美麗的，我的弟弟啊。」<br>
<br>
啾啾們知道了，現在他們的任務就是飛到兩姐弟的肩膀上，用毛絨絨的身體治癒兩人的心靈。<br>
<br>
「今後我們會繼續喜歡漫畫。」<br>
「同時也會繼續喜歡咖哩。」<br>
<br>
啾啾警探事件簿，早春的漫研社事件，結案。啾。


